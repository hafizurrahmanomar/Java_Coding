package data_Type;

public class TypeCasting {
    public static void main(String[] args) {

        int a = 8;
        double b= a;// Automatic casting: int to double
        float height = 70.0f;
        double height1 = height;
        System.out.println(height1);
        System.out.println(b);
        System.out.println(a);
        
        
    }
    
}
